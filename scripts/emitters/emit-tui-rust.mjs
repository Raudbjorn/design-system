// Emitter for crates/raudbjorn-tui/src/theme/generated.rs

export const emitTuiRust = (themes) => {
  let content = `// DO NOT EDIT: generated by scripts/emitters/emit-tui-rust.mjs
// Source themes: ${themes.map((t) => t.name).join(', ')}

use ratatui::style::Color;

`;

  const hexToRgb = (hex, field, themeName) => {
    if (!/^#[0-9a-f]{6}$/i.test(hex)) {
        throw new Error(`Theme ${themeName} field ${field} has invalid hex: ${hex}`);
    }
    const r = parseInt(hex.slice(1, 3), 16);
    const g = parseInt(hex.slice(3, 5), 16);
    const b = parseInt(hex.slice(5, 7), 16);
    return `Color::Rgb(${r}, ${g}, ${b})`;
  };

  const fields = [
    'bg', 'surface-1', 'surface-2', 'surface-3', 'border', 'text', 'text-strong',
    'text-muted', 'text-faint', 'accent', 'accent-2', 'accent-rust', 'mix-target',
    'success', 'error', 'warning', 'syn-keyword', 'syn-string', 'syn-var',
    'syn-func', 'syn-comment', 'syn-number'
  ];

  for (const theme of themes) {
    const paletteHex = theme.paletteHex;
    const name = theme.name.toUpperCase();
    content += `pub const ${name}: TerminalPalette = TerminalPalette {\n`;
    for (const field of fields) {
      const rustField = field.replace(/-/g, '_');
      const val = paletteHex[field];
      if (!val) {
        throw new Error(`Theme ${theme.name} missing field ${field}`);
      }
      content += `    ${rustField}: ${hexToRgb(val, field, theme.name)},\n`;
    }
    content += `};\n\n`;
  }
  return `${content.trimEnd()}\n`;
};
